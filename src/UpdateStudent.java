    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String username = txtUsername.getText();
        String firstname = txtFirstname.getText();
        String lastname = txtLastname.getText();
        String age = txtAge.getText();
        String phoneNumber = txtPhoneNumber.getText();
        String degree = txtDegree.getText();
        String password = txtPassword.getText();
        String confirmPassword = txtConfirmPassword.getText();
        
        if(phoneNumber.matches("^[0-9]*$") && phoneNumber.length() == 10 && age.matches("^[0-9]*$") && age.length() <= 2){
            if(username.equals("") || firstname.equals("") || lastname.equals("") || degree.equals("") || password.equals("") || confirmPassword.equals("")){
            JOptionPane.showMessageDialog(null, "Please enter correct student information!", "Alert !", JOptionPane.ERROR_MESSAGE);
        }else{
            if(password.equals(confirmPassword)){
               try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/javaproject2", "root", "");
            
            String queryStr = "UPDATE student SET firstname = '"+firstname+"', lastname = '"+lastname+"', age = '"+age+"', phone_number = '"+phoneNumber+"', degree = '"+degree+"', password = '"+password+"'"
                    + "WHERE username = '"+username+"'";
            Statement stmt = con.createStatement();
            stmt.executeUpdate(queryStr);
            
            JOptionPane.showMessageDialog(null, "Update is Successful!", "Alert !", JOptionPane.INFORMATION_MESSAGE);
            
            stmt.close();
            con.close();
        
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Sql Error Occured! " + e, "Alert !", JOptionPane.ERROR_MESSAGE);
        }
        catch (Exception ex){
            JOptionPane.showMessageDialog(null, "Error Occured! " + ex, "Alert !", JOptionPane.ERROR_MESSAGE);
        } 
            }
            else{
                JOptionPane.showMessageDialog(null, "Password is not matched!", "Alert !", JOptionPane.ERROR_MESSAGE);
            }
            
        }
        }else{
            JOptionPane.showMessageDialog(null, "Please enter correct student information!", "Alert !", JOptionPane.ERROR_MESSAGE);
        }
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

